async function addCorrectionQuestions() {
  try {
    console.log('正在添加错别字纠错题目...');

    for (const q of correctionQuestions) {
      await Question.create({
        type: 'correction',
        year: 2024,
        source: '同步练习题库',
        content: q.content,
        options: q.options,
        answer: q.answer,
        explanation: q.explanation,
        difficulty: 'medium'
      });
    }

    console.log(`成功添加 ${correctionQuestions.length} 道错别字纠错题目！`);

    // 统计总数
    const total = await Question.count({ where: { type: 'correction' } });
    console.log(`\n错别字纠错题目总数: ${total} 道`);

  } catch (error) {
    console.error('添加题目失败:', error);
  }
}

